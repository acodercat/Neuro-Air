"""Category-level ablation outcome accounting (Extended Data table source).

Derives, from the validated per-task matrix (pv_matrix.json == Table 2), the
category-level wins/losses backing the main-text repair-rate claims, asserts
every published number, and prints the LaTeX table rows.

Published claims verified here:
  - per-task-pair repair rates 11.1% (spatial) and 11.3% (statistical),
    4.4--7.0% elsewhere;
  - pooled discordants 38 wins / 9 losses;
  - six tasks repaired under >= 2 backbones;
  - all nine losses fall in spatial analysis.
"""
import json
from collections import defaultdict
from decimal import Decimal, ROUND_HALF_UP

d = json.load(open('experiments/Neuro-Air/pv_matrix.json'))
reg = json.load(open('benchmarks_neuroair_ablation.json'))
cat_of = {nm: cat for dom, cats in reg.items() for cat, items in cats.items() for nm in items}

W = defaultdict(int); L = defaultdict(int); N = defaultdict(int)
multiwin = {}
for t, mm in d['matrix'].items():
    N[cat_of[t]] += 1
    wins_here = 0
    for m, c in mm.items():
        if c['multi'] == 'P' and c['single'] == 'F': W[cat_of[t]] += 1; wins_here += 1
        if c['multi'] == 'F' and c['single'] == 'P': L[cat_of[t]] += 1
    if wins_here >= 2: multiwin[t] = wins_here

def rate(w, pairs):  # half-up to one decimal, matching prose (9/80 -> 11.3)
    return Decimal(100 * w / pairs).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)

# --- assertions against published numbers ---
assert (N['spatial_analysis'], W['spatial_analysis'], L['spatial_analysis']) == (27, 15, 9)
assert (N['statistical_characterization'], W['statistical_characterization']) == (16, 9)
assert (N['data_retrieval'], W['data_retrieval']) == (20, 7)
assert (N['comparative_assessment'], W['comparative_assessment']) == (18, 5)
assert (N['temporal_pattern_analysis'], W['temporal_pattern_analysis']) == (9, 2)
assert sum(W.values()) == 38 and sum(L.values()) == 9
assert sum(L[c] for c in L if c != 'spatial_analysis') == 0, "losses outside spatial"
assert len(multiwin) == 6 and max(multiwin.values()) == 3
assert str(rate(15, 135)) == '11.1' and str(rate(9, 80)) == '11.3'

LABEL = {'spatial_analysis': 'Spatial analysis', 'data_retrieval': 'Data retrieval',
         'comparative_assessment': 'Comparative assessment',
         'statistical_characterization': 'Statistical characterization',
         'temporal_pattern_analysis': 'Temporal pattern analysis'}
order = sorted(N, key=lambda c: -N[c])
print("% rows for tab:ablation_categories (generated by scripts/_category_table.py)")
for c in order:
    p = N[c] * 5
    print(f"{LABEL[c]} & {N[c]} & {p} & {W[c]} & {L[c]} & {rate(W[c], p)}\\% \\\\")
print(f"\\midrule")
print(f"\\textbf{{Total}} & 90 & 450 & 38 & 9 & {rate(38, 450)}\\% \\\\")
print(f"\n% multi-backbone repairs: {sorted(multiwin.items())}")
print("ALL ASSERTIONS PASSED")
